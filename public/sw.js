// Offline service worker for Budget Planner.
//
// Strategy:
//   - Navigations: network-first, falling back to the cached app shell so the
//     app still opens with no connection.
//   - Same-origin assets (JS/CSS/icons): stale-while-revalidate — serve the
//     cached copy instantly, refresh it in the background.
//   - Cross-origin requests (Supabase, etc.) are left untouched so cloud sync
//     and auth behave exactly as before.
//
// State lives in localStorage, so once the shell is cached the whole budget is
// usable offline.

const CACHE = "budget-planner-v1";

self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k))),
      )
      .then(() => self.clients.claim()),
  );
});

self.addEventListener("fetch", (event) => {
  const req = event.request;
  if (req.method !== "GET") return;

  const url = new URL(req.url);
  if (url.origin !== self.location.origin) return; // don't touch Supabase/CDN/etc.

  if (req.mode === "navigate") {
    event.respondWith(
      fetch(req)
        .then((res) => {
          const copy = res.clone();
          caches.open(CACHE).then((c) => c.put(req, copy));
          return res;
        })
        .catch(() =>
          caches.match(req).then((cached) => cached || caches.match("/")),
        ),
    );
    return;
  }

  event.respondWith(
    caches.match(req).then((cached) => {
      const network = fetch(req)
        .then((res) => {
          if (res && res.status === 200) {
            const copy = res.clone();
            caches.open(CACHE).then((c) => c.put(req, copy));
          }
          return res;
        })
        .catch(() => cached);
      return cached || network;
    }),
  );
});
